package doc.system.entity;
public class Parameter {
	private String id;
	private String lei;
	private String jian;
	private String zhi;
	private String xu;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getLei() {
		return lei;
	}
	public void setLei(String lei) {
		this.lei = lei;
	}
	public String getJian() {
		return jian;
	}
	public void setJian(String jian) {
		this.jian = jian;
	}
	public String getZhi() {
		return zhi;
	}
	public void setZhi(String zhi) {
		this.zhi = zhi;
	}
	public String getXu() {
		return xu;
	}
	public void setXu(String xu) {
		this.xu = xu;
	}
}
